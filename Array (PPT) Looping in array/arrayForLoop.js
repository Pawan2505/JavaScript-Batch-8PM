// The for loop is a traditional way of iterating over an array. You have complete control over the loop, including the starting point, the ending point, and the increment step.


let numbers = [1, 2, 3, 4, 5];

for (let i = 0; i < numbers.length; i++) {
  console.log(numbers[i]);
}





// for Loop: Traditional, full control
// for...of Loop: Modern, simpler syntax
// forEach Method: Executes function on each element
// map Method: Creates new array, calls function on each element
// while Loop: Runs while condition is true
// do...while Loop: Executes block once, then checks condition